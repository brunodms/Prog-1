public class Ex2 {
    public static void main(String args[]){
        int i1 = 8;
        int i2 = 11;
        int somaint = i1 + i2;
        System.out.printf("\n %s: %2d + %2d = %2d", "Int", i1, i2, somaint);
        System.out.println("\n");

        double f1 = 8.8;
        double f2 = 11.11;
        double somadouble = f1 + f2;
        System.out.printf("\n %s: %.2f + %.2f = %.2f", "Double", f1, f2, somadouble);
    }
}
